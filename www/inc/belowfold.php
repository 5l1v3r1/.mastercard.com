						<h3>A Global Market Assessment</h3>
						<div class="subcolumns">
							<div class="c50l">
								<div class="padl">
									<p>While it's early days for the adoption of mobile payments globally, some markets are making progress toward attaining the right mix of market forces and consumer acceptance. That's among the top-level findings of the MasterCard Mobile Payments Readiness Index (MPRI), a data-driven survey of the global mobile payments landscape. Using public and proprietary data, as well as original market research, the survey gauges the preparedness and receptivity of 34 countries for mobile payments of three varieties &ndash; person to </p>
								</div>
							</div>
							<div class="c50r">
								<p>person (P2P), mobile e-commerce (m-commerce), and mobile payments at the point of sale (POS).</p>
								<p>The MPRI runs on a scale of zero to 100, with 100 representing complete replacement of plastic cards by mobile devices, an unlikely scenario. In MasterCard's judgment, the point of inflection&mdash;the stage at which mobile devices account for an appreciable share of the payments mix&mdash;is a Mobile Readiness score of 60. The 34 countries in the current Index achieved an average score of 33.2. No market reached 50, indicating there is work to be done before mobile payments become mainstream.</p>
