						<h3>Other top-level findings</h3>
						<div class="subcolumns top-findings">
							<div class="c25l">
								<h4>Conditions<br />Vary Greatly</h4>
								<p style="padding-right:20px">No two global markets are identical; each is driven by local conditions of financial services, infrastructure, environment, and regulation.</p>
							</div>
							<div class="c25l">
								<h4>M-Commerce<br />Leads</h4>
								<p style="padding-right:20px">In the vast majority of markets, more consumers are currently using mobile payments for m-commerce than for person-to-person (P2P) or point-of-sale (POS) transactions.</p>
							</div>
							<div class="c25l">
								<h4>Different Segments,<br />Different Needs</h4>
								<p style="padding-right:20px">Consumers are typically drawn to mobile payments either for access to electronic payments (mainly in developing economies) or the convenience of mobile phone payments (in the developed world).</p>
							</div>
							<div class="c25l">
								<h4>Emerging<br />Markets Ahead</h4>
								<p>Conditions on the ground, such as a stable telco network, established financial services, and progressive regulation, do not ensure consumer readiness &ndash; the one essential variable &ndash; in any given market.</p>
							</div>
